      measure2: json["strMeasure2"] ?? "",
      measure3: json["strMeasure3"] ?? "",
      measure4: json["strMeasure4"] ?? "",
      measure5: json["strMeasure5"] ?? "",
      measure6: json["strMeasure6"] ?? "",
      measure7: json["strMeasure7"] ?? "",
      measure8: json["strMeasure8"] ?? "",
    );
  }
}
